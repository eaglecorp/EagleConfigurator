        private MSTt01_medio_pago GetObjeto()
        {
            var obj = new MSTt01_medio_pago();
            try
            {
                obj.txt_desc = txtNombre.Text.Trim();
                obj.cod_medio_pago = txtCodigo.Text.Trim();

                obj.req_ref = chkRequiereRef.Checked;
                obj.id_estado = chkActivo.Checked ? Estado.IdActivo : Estado.IdInactivo;
                obj.txt_estado = chkActivo.Checked ? Estado.TxtActivo : Estado.TxtInactivo;


                if (cboTipoMedioPago.SelectedValue != null)
                    obj.id_tipo_medio_pago = int.Parse(cboTipoMedioPago.SelectedValue.ToString());
                else obj.id_tipo_medio_pago = null;
            }
            catch (Exception e)
            {
                MessageBox.Show(this, "Excepción en el Get: " + e.Message);
            }

            return obj;
        }
        private void SetObjeto(MSTt01_medio_pago obj)
